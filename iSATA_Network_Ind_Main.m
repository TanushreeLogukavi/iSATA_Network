
%%  Step by Step Description of the Main file is given Below
% 1. This is the Main file to perform montage simulation Using ROAST
% 2. Then It will Perform AC-PC detection
% 3. iSATA  Electric Field Distribution will be provided for each AAL-116 ROIs
% 4. Dose Target Determination Index (DTDI) of the target areas will be provided
% Step 1-4 are from previous i-SATA MNI Version
%% Step 5 -ISATA Network Analysis Starts Here- 
% Electric Field intensity received by each network (AAL-116 atlas is divided into 9 Networks)
% Average Current Intensity for each Network- is shown as (NwCD)
% The Dose Network target engagement (DNTE)- Provides an index of the current
% intensity received by each network (in both hemispheres, and each
% hemisphere) Please refer to (Hu et al., 2024)
% Created By - Tanushree L Date -12 March, 2026- at NIMHANS, Bangalore
% tanushreelogukavi@gmail.com
